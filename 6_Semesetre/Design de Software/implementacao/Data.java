public class Data {
	int dia;
	int mes;
	int ano;

	public int getDia() {
		return this.dia;
	}
	public void setDia(int dia) {
		this.dia = dia;
	}
	
	public int getMes() {
		return this.mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	
	public int gatAno() {
		return this.ano;
	}
	public void satAno(int ano) {
		this.ano = ano;
	}
	
}
